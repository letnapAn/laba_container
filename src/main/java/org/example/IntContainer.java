package org.example;

/**
 * Интерфейс, определяющий контракт для контейнера целых чисел.
 * <p>
 * Данный интерфейс описывает базовые операции, которые должны поддерживать
 * все реализации контейнера. Он расширяет Iterable, что позволяет
 * использовать контейнер в цикле for-each.
 * </p>
 *
 * <h2>Основные операции (согласно ТЗ):</h2>
 * <ul>
 *   <li>add(int) — добавляет значение в контейнер</li>
 *   <li>contains(int) — проверяет наличие значения</li>
 *   <li>remove(int) — удаляет значение из контейнера</li>
 * </ul>
 *
 * <h2>Дополнительные операции (для удобства использования):</h2>
 * <ul>
 *   <li>size() — возвращает текущее количество элементов</li>
 *   <li>clear() — полностью очищает контейнер</li>
 * </ul>
 *
 * @see IntHashSet Конкретная реализация контейнера на основе хэш-таблицы
 * @see Iterable Базовый интерфейс для поддержки итерации
 */
public interface IntContainer extends Iterable<Integer> {

    /**
     * Добавляет указанное значение в контейнер.
     * <p>
     * Поведение метода зависит от конкретной реализации:
     * <ul>
     *   <li>Для множеств (Set) — добавляет только уникальные значения</li>
     *   <li>Для списков (List) — может добавлять дубликаты</li>
     * </ul>
     *
     * @param value целое число для добавления
     * @return true, если значение было добавлено,
     *         false, если значение не может быть добавлено
     *         (например, при дубликате в множестве)
     */
    boolean add(int value);

    /**
     * Проверяет, содержится ли указанное значение в контейнере.
     *
     * @param value значение для поиска
     * @return true, если значение найдено, false в противном случае
     */
    boolean contains(int value);

    /**
     * Удаляет указанное значение из контейнера
     *
     * @param value значение для удаления
     * @return true, если значение было найдено и удалено,
     *         false, если значение не найдено
     */
    boolean remove(int value);

    /**
     * Возвращает текущее количество элементов в контейнере.
     *
     * @return количество элементов (всегда неотрицательное)
     */
    int size();

    /**
     * Удаляет все элементы из контейнера.
     * <p>
     * После выполнения этого метода {@link #size()} возвращает 0.
     * Емкость контейнера может быть сброшена к начальному значению
     */
    void clear();
}